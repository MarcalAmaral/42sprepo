int	ft_strcmp(char *s1, char *s2)
{	
	while (*s2 || *s1)
	{
		if (*s1 < *s2)
		{
			return (-1);
		}
		else if (*s1 > *s2)
		{
			return (1);
		}
		s2++;
		s1++;
	}
	return (0);
}
